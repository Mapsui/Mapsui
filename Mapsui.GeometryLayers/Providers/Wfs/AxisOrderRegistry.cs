        /// <summary>
        /// Gets the axis order for *all* EPSG defined coordinate reference systems with an srid less than 32768
        /// </summary>
        /// <param name="identifier">The identifier</param>
        /// <returns>The axis order</returns>
        public int[] this[string identifier]
        {
            get
            {
                var authority = "EPSG";
                if (identifier.Contains(":"))
                {
                    authority = identifier.Split(':')[0].Trim();
                    identifier = identifier.Split(':')[1].Trim();
                }


                var res = new int[2];
                switch (authority)
                {
                    //case "OGC":
                    default:
                        Buffer.BlockCopy(Unusual, 0, res, 0, 2);
                        return res;

                    case "EPSG":
                        var code = int.Parse(identifier);
                        if (code == 900913) code = 3857;
                        if (code < 0)
                            throw new ArgumentException("Invalid Epsg identifier");

                        var byteIndex = code / 8;
                        var bitIndex = code % 8;
                        var flag = 1 << bitIndex;//1 << (7 - bitIndex);
                        // BlockCopy copies the bytes so length is 8 (2 ints)
                        Buffer.BlockCopy((EpsgAxisOrderBitField[byteIndex] & flag) == flag ? Unusual : Usual, 0, res, 0, 8);
                        return res;

                }
            }
        }

