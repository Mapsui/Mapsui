namespace Mapsui;

public enum MBoxFit
{
    /// <summary>
    ///     Fit within the view port of the screen
    /// </summary>
    Fit,

    /// <summary>
    ///     Fill up the entire view port of the screen
    /// </summary>
    Fill,

    /// <summary>
    ///     Fill the width of the screen
    /// </summary>
    FitWidth,

    /// <summary>
    ///     Fill the height of the screen
    /// </summary>
    FitHeight
}
